//1.swap number without using 3 variable

public class Swap_Number_Without_Using_3Variable {
	
	
	public static void main (String args[]) {
		int a=10,b=20;
		a=a+b;
		b=a-b;
		a=a-b;
		
		System.out.println("a ="+a+" b="+b);
	}

}     


